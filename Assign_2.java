
public class Assign_2 {

	
	public static void main(String[] args) {
	
		int a=20;
		int b=10;
		int x= a-- - --a;
		int y=a--;
		int z=a>>2;
		int u=a&b;
		
		System.out.println("a-- - --a =" + " "+ x);
		System.out.println("a-- =" + " "+ y);
		System.out.println("a>>2 =" + z);
		System.out.println("a&b =" + u);
		
	}

}
